#!/usr/bin/env node
// build-draft-profile.cjs — regenerate js/shared/draft-profile-data.js
//
// NFL draft capital for ALL players (vets included), keyed by Sleeper id.
// Sleeper's own players payload has no draft data (the reason the roster
// board's Draft/Drafted columns were rookie-only), so we vendor a static
// historical dataset instead — draft capital never changes once it happens.
//
// Sources (both openly licensed):
//   1. DynastyProcess db_playerids.csv — the sleeper_id ↔ gsis_id crosswalk
//      (plus draft_year/round/pick as a fallback when nflverse lacks a row)
//   2. nflverse players.csv — authoritative draft_year/round/pick/team by
//      gsis_id; a present row with an empty draft_round means true UDFA.
//
// Usage:
//   node scripts/build-draft-profile.cjs                # fetch from the web
//   node scripts/build-draft-profile.cjs <ids.csv> <players.csv>   # local files
//
// Output format (compact — the file ships as a plain script tag):
//   window.WR_DRAFT_PROFILE = { "<sleeper_id>": [year, round, overall, "TEAM"], ... }
//   `overall` is the OVERALL selection number (nflverse draft_pick), not the
//   pick-within-round. round 0 = confirmed UDFA (year = entry season when known).

const fs = require('fs');
const path = require('path');

const IDS_URL = 'https://raw.githubusercontent.com/dynastyprocess/data/master/files/db_playerids.csv';
const PLAYERS_URL = 'https://github.com/nflverse/nflverse-data/releases/download/players/players.csv';
const OUT = path.join(__dirname, '..', 'js', 'shared', 'draft-profile-data.js');

// Minimal RFC-4180 CSV parser (quoted fields, embedded commas/newlines).
function parseCsv(text) {
  const rows = [];
  let row = [], field = '', inQ = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQ) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQ = false;
      } else field += c;
    } else if (c === '"') inQ = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n') { row.push(field.replace(/\r$/, '')); rows.push(row); row = []; field = ''; }
    else field += c;
  }
  if (field.length || row.length) { row.push(field.replace(/\r$/, '')); rows.push(row); }
  return rows;
}

function indexBy(header) {
  const idx = {};
  header.forEach((h, i) => { idx[h] = i; });
  return idx;
}

async function load(urlOrPath, fallbackUrl) {
  if (urlOrPath && fs.existsSync(urlOrPath)) return fs.readFileSync(urlOrPath, 'utf8');
  const url = urlOrPath || fallbackUrl;
  const res = await fetch(url, { redirect: 'follow' });
  if (!res.ok) throw new Error('fetch failed ' + res.status + ' for ' + url);
  return res.text();
}

(async () => {
  const [idsText, playersText] = await Promise.all([
    load(process.argv[2], IDS_URL),
    load(process.argv[3], PLAYERS_URL),
  ]);

  const idsRows = parseCsv(idsText);
  const idsIdx = indexBy(idsRows[0]);
  const plRows = parseCsv(playersText);
  const plIdx = indexBy(plRows[0]);

  // gsis_id → { y, rd, pk, tm } from nflverse (authoritative).
  const byGsis = new Map();
  for (let i = 1; i < plRows.length; i++) {
    const r = plRows[i];
    const gsis = r[plIdx.gsis_id];
    if (!gsis) continue;
    const rd = parseInt(r[plIdx.draft_round], 10);
    const y = parseInt(r[plIdx.draft_year], 10) || parseInt(r[plIdx.rookie_season], 10) || 0;
    byGsis.set(gsis, isNaN(rd) || rd < 1
      ? { y, rd: 0, pk: 0, tm: '' }                       // in the NFL, never drafted → UDFA
      : { y, rd, pk: parseInt(r[plIdx.draft_pick], 10) || 0, tm: r[plIdx.draft_team] || '' });
  }

  const out = {};
  let joined = 0, fallback = 0, udfa = 0;
  for (let i = 1; i < idsRows.length; i++) {
    const r = idsRows[i];
    const sleeper = r[idsIdx.sleeper_id];
    if (!sleeper || sleeper === 'NA' || !/^\d+$/.test(sleeper)) continue; // skip team DEF / missing
    const gsis = r[idsIdx.gsis_id];
    let rec = gsis && gsis !== 'NA' ? byGsis.get(gsis) : null;
    if (rec) joined++;
    else {
      // Crosswalk-only fallback (mostly incoming rookies nflverse hasn't
      // ingested yet). Empty draft_round here = unknown, NOT proven UDFA —
      // skip so the UI keeps its honest '—'.
      const rd = parseInt(r[idsIdx.draft_round], 10);
      if (isNaN(rd) || rd < 1) continue;
      rec = { y: parseInt(r[idsIdx.draft_year], 10) || 0, rd, pk: parseInt(r[idsIdx.draft_pick], 10) || 0, tm: '' };
      fallback++;
    }
    if (rec.rd === 0) udfa++;
    out[sleeper] = [rec.y, rec.rd, rec.pk, rec.tm];
  }

  const entries = Object.keys(out).sort((a, b) => +a - +b)
    .map(k => JSON.stringify(k) + ':' + JSON.stringify(out[k]));
  const body =
    '// GENERATED by scripts/build-draft-profile.cjs — do not hand-edit.\n' +
    '// NFL draft capital by Sleeper player id: [year, round, OVERALL pick, "TEAM"].\n' +
    '// round 0 = confirmed UDFA. Sources: DynastyProcess db_playerids +\n' +
    '// nflverse players (see the script header for URLs/licenses).\n' +
    'window.WR_DRAFT_PROFILE = {\n' + entries.join(',\n') + '\n};\n';
  fs.writeFileSync(OUT, body, 'utf8');
  console.log('Wrote ' + entries.length + ' players → ' + path.relative(process.cwd(), OUT) +
    ' (' + (body.length / 1024).toFixed(0) + ' KB; nflverse-joined ' + joined +
    ', crosswalk-only ' + fallback + ', UDFA ' + udfa + ')');
})().catch(e => { console.error(e); process.exit(1); });
